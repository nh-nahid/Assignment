// A Function of Converting Celsius To Fahrenheit

function toFahrenheit (tempInCelsius) {

return ((tempInCelsius * 9 / 5) + 32);
}

console.log( toFahrenheit(45));


// A Function of Converting Fahrenheit To Celsius

function toCelsius (tempInFahrenheit) {

 return ( 5 * (( tempInFahrenheit - 32)) / 9);
}

console.log(toCelsius(83.7));